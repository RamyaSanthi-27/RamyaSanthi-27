package week1.day2;



public class NegativetoPositive {
	public static void main(String[] args) {
		int num = -40;
		
		if (num<0) {
			int convert=num*(-1);
			System.out.println("Number "+num+" is a negative number" );
			System.out.println("Number "+num+" is converted into "+convert+ " Positive Number" );
		}
	
		
	}

}

